  // ─── HELPER METHODS ────────────────────────────────
  static List<Tool> getToolsByCategory(String categoryId) {
    return tools.where((t) => t.categoryId == categoryId).toList();
  }

  static List<Tool> getFreeTools() {
    return tools.where((t) => t.hasFreeTier).toList();
  }

  static List<Tool> getFeaturedTools() {
    return tools.where((t) => t.isFeatured).toList();
  }

  static List<Tool> getNewTools() {
    return tools.where((t) => t.isNew).toList();
  }

  static List<Tool> getTrendingTools() {
    final sorted = List<Tool>.from(tools)..sort((a, b) => b.clickCount.compareTo(a.clickCount));
    return sorted.take(10).toList();
  }

  static List<Tool> searchTools(String query) {
    final q = query.toLowerCase();
    return tools.where((t) =>
      t.name.toLowerCase().contains(q) ||
      t.description.toLowerCase().contains(q) ||
      t.categoryId.toLowerCase().contains(q)
    ).toList();
  }

  static int getToolCountForCategory(String categoryId) {
    return tools.where((t) => t.categoryId == categoryId).length;
  }

  static int getFreeToolCountForCategory(String categoryId) {
    return tools.where((t) => t.categoryId == categoryId && t.hasFreeTier).length;
  }
